#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		n;
	long	m;
	char	*p;

	p = (char *)str;
	n = 1;
	i = 0;
	m = 0;
	while ((p[i] >= 9 && p[i] <= 13) || p[i] == 32)
		i++;
	if (p[i] == '-' || p[i] == '+')
	{
		if (p[i++] == '-')
			n *= -1;
	}
	while (p[i] >= '0' && p[i] <= '9')
	{
		m = (m * 10) + (p[i++] - 48);
		if (m > 2147483648 && n == -1)
			return (0);
		else if (m > 2147483647 && n == 1)
			return (-1);
	}
	return (m *= n);
}
